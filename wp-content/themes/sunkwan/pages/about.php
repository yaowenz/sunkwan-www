				<p>盈信投资集团是上坤集团的投资股东，成立于 2003 年，由中国著名投资人林劲峰先生一手创办，他同股神巴菲特私交甚厚，在短短数年间其投资总额超百亿元。他从不进行风险投资，用自己独特的价值理念与投资策略，创造了 26 年连续盈利的业界神话。盈信投资集团的主要投资方向为“大消费”、“互联网大消费”及“金融行业”，目前投资涉及房地产、快消品、食品酒类、金融、互联网信息等领域多家上市公司，其中已经投资贵州茅台股票、西凤酒、招商银行、中国银联、朗诗地产、上坤集团等优质企业。</p>
